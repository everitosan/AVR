const unsigned char sable[5][8] = {
	{ 0xC3,0xE3,0xF3,0xDB,0xCF,0xC7,0xC3,0xC1}, //S
	{ 0x00,0x3F,0x7F,0xCC,0xCC,0x7F,0x3F,0x00}, //A
	{ 0x00,0xFF,0xFF,0x33,0x33,0x33,0x3F,0x1E}, //B
	{ 0xFF,0xFF,0x03,0x03,0x03,0x03,0x03,0x03}, //L
	{ 0xFF,0xFF,0xDB,0xDB,0xDB,0xDB,0xDB,0xDB} //E
};

unsigned char i=0, j=0, duracion;
int corrimiento  = 0b10000000;

int main(void)
{
	DDRD=0xFF;
	DDRB=0xFF;

  while(1)
  {
  	for(duracion=0; duracion<70; duracion++) {
	  	for(i=0; i<8;i++)
			{
				PORTB = ~corrimiento;	
				PORTD=sable[j][i];		
				_delay_ms(1);
				corrimiento = corrimiento>>1;
			}		
			i=0;	
			corrimiento  = 0b10000000;
  	}
  	j++;

  	if(j==5) {
  		j=0;
  	}

  }
}
